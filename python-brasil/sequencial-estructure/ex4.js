// Faça um Programa que peça o raio de um círculo, calcule e mostre sua área.
// do a program that requests the ray of a circle, calculate and show your area

let rayOfCircle = prompt('Informe o raio do círculo')
rayOfCircle = Number(rayOfCircle)

const areaCircle = 3.14 * (rayOfCircle * rayOfCircle)
alert(`A área do círculo é igual a ${areaCircle}cm²`)
